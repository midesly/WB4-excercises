// while loop example

let num = 1;
let i = 1;

while (i < 5){
    num = num * 2;
    console.log(num);
    i++;
}

// do/while loop examples

 do {
    num = num * 2;
    console.log(num);
    i++;
 } while(i < 5);

 // for loop example

 for(i = 0; i < 5; i++){
    num = num * 2;
    console.log(num);
 }